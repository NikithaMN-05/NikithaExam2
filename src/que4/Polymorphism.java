public class Polymorphism {

    public int sum(int i1, int i2) {
        return i1 + i2;
    }

    public double sum(double d1, double d2) {
        return d1 + d2;
    }

    public int sum(int i1, int i2, int i3) {
        return i1 + i2 + i3;
    }

    public double sum(double d1, double d2, double d3) {
        return d1 + d2 + d3;
    }
}
